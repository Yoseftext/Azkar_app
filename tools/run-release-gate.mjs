import { spawn } from 'node:child_process';
import { mkdir, writeFile } from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const reportPath = path.join(projectRoot, 'RELEASE_READINESS_REPORT.md');
const reportsDir = path.join(projectRoot, 'artifacts', 'release-gate');
const generatedAt = new Date().toISOString();
const safeStamp = generatedAt.replace(/[:.]/g, '-');
const runDir = path.join(reportsDir, safeStamp);

const steps = [
  ['static-check', ['npm', 'run', 'static:check']],
  ['typecheck', ['npm', 'run', 'typecheck']],
  ['lint', ['npm', 'run', 'lint']],
  ['format-check', ['npm', 'run', 'format:check']],
  ['test', ['npm', 'test']],
  ['build', ['npm', 'run', 'build']],
  ['chunk-audit', ['npm', 'run', 'audit:chunks']],
  ['browser-runtime', ['npm', 'run', 'verify:browser']],
  ['real-browser-e2e', ['npm', 'run', 'verify:e2e']],
];

await mkdir(runDir, { recursive: true });

function statusForCode(code) {
  return code === 0 ? 'passed' : code === 2 ? 'blocked-by-environment' : 'failed';
}

async function writeReport(results) {
  const lines = [
    '# Release Readiness Report',
    '',
    `Generated at: ${generatedAt}`,
    `Host: ${os.platform()} ${os.release()}`,
    `Logs directory: ${path.relative(projectRoot, runDir)}`,
    '',
    '> This report is generated by `npm run verify:release`. Do not treat stale, hand-edited report content as a source of truth.',
    '',
    '## Step results',
  ];

  for (const result of results) {
    lines.push(`- ${result.name}: ${statusForCode(result.code)}`);
  }

  lines.push('', '## Log files');
  for (const result of results) {
    lines.push(`- ${result.name}: ${path.relative(projectRoot, result.logPath)}`);
  }

  if (results.some((result) => result.name === 'real-browser-e2e' && result.code === 2)) {
    lines.push('', '## Note', '- Real-browser E2E is prepared but blocked by Chromium policy in the current environment.');
  }

  await writeFile(reportPath, lines.join('\n') + '\n');
}

async function runStep(name, command, logPath) {
  return new Promise((resolve) => {
    const proc = spawn(command[0], command.slice(1), {
      cwd: projectRoot,
      shell: false,
      stdio: ['ignore', 'pipe', 'pipe'],
    });

    let output = '';
    proc.stdout.on('data', (chunk) => {
      output += chunk.toString();
    });
    proc.stderr.on('data', (chunk) => {
      output += chunk.toString();
    });
    proc.on('error', async (error) => {
      output += `\n[spawn-error:${name}] ${String(error)}\n`;
      await writeFile(logPath, output);
      resolve(1);
    });
    proc.on('close', async (exitCode) => {
      await writeFile(logPath, output);
      resolve(exitCode ?? 1);
    });
  });
}

const results = [];
for (const [name, command] of steps) {
  const logPath = path.join(runDir, `${name}.log`);
  process.stdout.write(`\n[verify:release] running ${name}...\n`);
  const code = await runStep(name, command, logPath);

  const result = { name, code, logPath };
  results.push(result);
  await writeReport(results);
  process.stdout.write(`[verify:release] ${name}: ${statusForCode(code)}\n`);

  if (code !== 0 && !(name === 'real-browser-e2e' && code === 2)) {
    process.exitCode = code;
    break;
  }
}
